package D19OOP;

public abstract class Vehicle {

 //    Soru 3:
//    Bir Vehicle abstract class oluştur:
//
//    Abstract method: startEngine() → void
//
//    Concrete method: fuel() → "Vehicle is fueling" yazsın
//
//    Car class’ı extend etsin, startEngine override etsin
//
//    main methodda Vehicle referansı ile Car object oluştur ve startEngine ve fuel methodlarını çağır*/


public abstract void startEngine();

    public void fuel(){
        System.out.println("Vehicle is fueling");
    }


























}
