/*
    Eğer override etmezsen, o zaman metodu değiştiremezsin.
    Yani parent’ta nasıl tanımlandıysa, aynı şekilde çalışır.
🧩 Neden böyle?
    Çünkü:
            extends = sadece miras almak demek → metodu kullanabilirsin.
    @Override = yeniden tanımlamak demek → metodu değiştirebilirsin.
    Yani:
            🔹 Override etmeden: sadece kullanırsın (davranış sabit)
🔹 Override edince: istediğin gibi değiştirebilirsin veya genişletebilirsin
super çağrısı ile de parenttaki methodu çağırıp override ile methodun üstüne ekleme yapabilirsin

🔍 Kısaca fark:
Durum	               Parent metodu	Child metodu	    Sonuç

Override  yok           	Kullanılır	        Yok	           Davranış değişmez

Override var	         Değiştirilir /
                          genişletilir	Yeni davranış geçerli	Davranış değişir

Override + super	Parent çağrılır,
                      üzerine ek yapılır	Genişletilmiş davranış	Parent + Child birlikte

*/




